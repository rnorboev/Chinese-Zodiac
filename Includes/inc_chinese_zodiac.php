<h1>The Chinese Zodiac</h1><br/>
<p >The Chinese zodiac is the classification scheme that assigns an animal to each year in a repeating 12-year cycle. The 12-year cycle of Chinese zodiac is an approximation to the 11.86-year orbital period of jupiter, the largest planet of the solar system. It is a scheme and systematic plan of future action that relates each year to an animal and its reputed attributes according to a 12-year cycle. It and its variations remain popular in several East Asian countries including China, Vietnam, Burma, Korea, Japan, Mongolia, Nepal, Bhutan, Sri Lanka, Cambodia, Laos and Thailand as well as the Buddhist calendar.</p><br />

<p>The Chinese zodiac has influenced different cultural zodiacs around the world. Several other countries have used it since many of them, particularly Asian, were under the influence of China at one time. The Chinese zodiac has been a great impact on zodiacs in other countries such as Japan, Vietnam, and Thailand.</p><br/>

<h2>Names</h2><br/>

<p>Identifying this scheme using the term "zodiac" reflects several superficial similarities to the Western zodiac: both have time cycles divided into 12 parts, each labels at least the majority of those parts with names of animals, and each is widely associated with a culture of ascribing a person's personality or events in his or her life to the supposed influence of the person's particular relationship to the cycle. Nevertheless, there are major differences: the Chinese 12-part cycle corresponds to years, rather than months. The Chinese zodiac is represented by 12 animals, whereas some of the signs in the Western zodiac are not animals, despite the implication of the Greek etymology of "zodiac". The animals of the Chinese zodiac are not associated with constellations spanned by the ecliptic plane.</p><br/>

